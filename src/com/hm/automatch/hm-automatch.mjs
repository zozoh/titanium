export default {
  ////////////////////////////////////////////////////
  props: {
    //------------------------------------------------
    // Data
    //------------------------------------------------
    "value": undefined
  },
  ////////////////////////////////////////////////////
  computed: {
    //------------------------------------------------
    TopClass() {
      return this.getTopClass({
        "is-nil": this.isNil,
        "no-nil": !this.isNil
      })
    },
    //------------------------------------------------
    isNil() {
      return Ti.Util.isNil(this.value)
    },
    //------------------------------------------------
    InfoText() {
      return JSON.stringify(this.value)
    }
    //------------------------------------------------
  }
  ////////////////////////////////////////////////////
}