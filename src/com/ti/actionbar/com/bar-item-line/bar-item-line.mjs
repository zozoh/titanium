export default {
  ///////////////////////////////////////
  inject: ["$bar"],
  ///////////////////////////////////////
  props : {
    "depth": {
      type: Number,
      default: 0
    }
  },
  ///////////////////////////////////////
  computed : {
    //-----------------------------------
    TopClass() {
      return this.getTopClass({
        "is-top" : this.depth == 1,
        "is-sub" : this.depth > 1
      })
    }
    //-----------------------------------
  }
  ///////////////////////////////////////
}