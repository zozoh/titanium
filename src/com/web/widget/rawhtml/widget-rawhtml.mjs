export default {
  /////////////////////////////////////////
  props : {
    //-----------------------------------
    // Data
    //-----------------------------------
    "html": {
      type : String,
      default: undefined
    }
  },
  /////////////////////////////////////////
  computed : {
    //------------------------------------
    TopClass() {
      return this.getTopClass()
    }
    //------------------------------------
  },
  /////////////////////////////////////////
  methods: {
    //-------------------------------------
    renderHTML(html = this.html) {
      if(this.$el) {
        this.$el.innerHTML = html || ""
      }
    }
    //-------------------------------------
  },
  /////////////////////////////////////////
  watch: {
    "html": {
      handler: function(newVal, oldVal){
        this.renderHTML(newVal)
      }
    }
  },
  /////////////////////////////////////////
  mounted: function() {
    this.renderHTML()
  }
  /////////////////////////////////////////
}